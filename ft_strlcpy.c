#include "libft.h"
/** The ft_strlcpy function copies up to size - 1 characters from the
NUL-terminated string src to dst, NUL-terminating the result.
take the full size of the buffer (not just the length) 
and guarantee to NUL-terminate unlike strncpy
in case when source string is to long only first characters will be copied -\0*/
size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	src_len;

	src_len = ft_strlen(src);
	if (src_len + 1 < dstsize)
		ft_memcpy(dst, src, src_len + 1);
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = 0;
	}
	return (src_len);
}
